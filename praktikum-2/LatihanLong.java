public class LatihanLong {
    public static void main(String args[]) {
    long p = 2147483648L;       //menambahkan karaktter 'L'
    System.out.println(p);
    }
    }

/*
1) Apa maksud dari pesan error yang muncul?
= Menyatakan bahwa bilangan 2147483648 terlalu besar untuk tipe data integer.

2) Bagaimana memperbaiki source code tersebut?
= Dengan menambahkan karakter L pada akhir bilangan, 
  yang berfungsi untuk mmenyatakan bahwa bilangan tersebut adalah jenis tipe data long.

*/